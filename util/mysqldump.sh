#!/bin/bash
SELF=`readlink -f $0`
SELF_DIR=`dirname $SELF`

APP_DIR="$SELF_DIR/../main"
if [ "$GEOGRAPHY_DATA_DIR" ]; then
	DATA_DIR="$GEOGRAPHY_DATA_DIR"
else
	DATA_DIR="$APP_DIR"
fi

mysqldump -p$GEOGRAPHY_DATABASE_PASSWORD -u$GEOGRAPHY_DATABASE_USER -h$GEOGRAPHY_DATABASE_HOST -P$GEOGRAPHY_DATABASE_PORT $GEOGRAPHY_DATABASE_NAME > $DATA_DIR/`date +"%Y-%m-%d_%H-%M-%S"`.sql
